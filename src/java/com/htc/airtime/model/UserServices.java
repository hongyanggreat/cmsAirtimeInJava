package com.htc.airtime.model;

import java.sql.Timestamp;

/**
 *
 * @author Admin
 */
public class UserServices {

    private int id;
    private String cpCode;
    private String gameName;
    private String codeGame;
    private String userName;
    private String servicePin;
    private String accessKey;
    private String secretKey;
    private int serviceType;
    private String urlCallback;
    private String ipAllow;
    private String routerTable;
    private String optionData;
    private String description;
    private int status;
    private Timestamp createDate;
    private int createBy;
    private Timestamp updateDate;
    private int updateBy;

    public UserServices() {
    }

    public UserServices(int id, String cpCode, String gameName, String codeGame, String userName, String servicePin, String accessKey, String secretKey, int serviceType, String urlCallback, String ipAllow, String routerTable, String optionData, String description, int status, Timestamp createDate, int createBy, Timestamp updateDate, int updateBy) {
        this.id = id;
        this.cpCode = cpCode;
        this.gameName = gameName;
        this.codeGame = codeGame;
        this.userName = userName;
        this.servicePin = servicePin;
        this.accessKey = accessKey;
        this.secretKey = secretKey;
        this.serviceType = serviceType;
        this.urlCallback = urlCallback;
        this.ipAllow = ipAllow;
        this.routerTable = routerTable;
        this.optionData = optionData;
        this.description = description;
        this.status = status;
        this.createDate = createDate;
        this.createBy = createBy;
        this.updateDate = updateDate;
        this.updateBy = updateBy;
    }

   

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCpCode() {
        return cpCode;
    }

    public void setCpCode(String cpCode) {
        this.cpCode = cpCode;
    }

    public String getGameName() {
        return gameName;
    }

    public void setGameName(String gameName) {
        this.gameName = gameName;
    }

    public String getCodeGame() {
        return codeGame;
    }

    public void setCodeGame(String codeGame) {
        this.codeGame = codeGame;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getServicePin() {
        return servicePin;
    }

    public void setServicePin(String servicePin) {
        this.servicePin = servicePin;
    }

    public String getAccessKey() {
        return accessKey;
    }

    public void setAccessKey(String accessKey) {
        this.accessKey = accessKey;
    }

    public String getSecretKey() {
        return secretKey;
    }

    public void setSecretKey(String secretKey) {
        this.secretKey = secretKey;
    }

    public int getServiceType() {
        return serviceType;
    }

    public void setServiceType(int serviceType) {
        this.serviceType = serviceType;
    }

    public String getUrlCallback() {
        return urlCallback;
    }

    public void setUrlCallback(String urlCallback) {
        this.urlCallback = urlCallback;
    }

    public String getIpAllow() {
        return ipAllow;
    }

    public void setIpAllow(String ipAllow) {
        this.ipAllow = ipAllow;
    }

    public String getRouterTable() {
        return routerTable;
    }

    public void setRouterTable(String routerTable) {
        this.routerTable = routerTable;
    }

    public String getOptionData() {
        return optionData;
    }

    public void setOptionData(String optionData) {
        this.optionData = optionData;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getCreateBy() {
        return createBy;
    }

    public void setCreateBy(int createBy) {
        this.createBy = createBy;
    }

    public int getUpdateBy() {
        return updateBy;
    }

    public void setUpdateBy(int updateBy) {
        this.updateBy = updateBy;
    }

    public Timestamp getCreateDate() {
        return createDate;
    }

    public void setCreateDate(Timestamp createDate) {
        this.createDate = createDate;
    }

    public Timestamp getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(Timestamp updateDate) {
        this.updateDate = updateDate;
    }

}
